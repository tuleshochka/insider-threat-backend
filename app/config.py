"""Конфигурация приложения через переменные окружения.
Pydantic-Settings позволяет переопределить любое поле через переменную окружения
(например, DB_HOST=10.0.0.1) без правки кода. Это стандарт 12-factor app.
"""
from pydantic_settings import BaseSettings


class Settings(BaseSettings):
    # ── Общие настройки ──────────────────────────────────────────────────
    app_name: str = "UEBA Monitor"
    # Значение по умолчанию — отображается в title OpenAPI-документации.
    # Если нужно другое имя — достаточно задать APP_NAME в .env.

    debug: bool = True
    # True на время разработки: FastAPI отдаёт подробные traceback'и.
    # Перед выкатом в production следует выставить False, чтобы не
    # светить внутреннее устройство системы потенциальному злоумышленнику.

    # ── Подключение к PostgreSQL ──────────────────────────────────────────
    db_host: str = "localhost"
    db_port: int = 5432
    db_user: str = "ueba"
    db_password: str = "ueba_pass"
    db_name: str = "ueba"
    # 5432 — стандартный порт PostgreSQL; localhost — логичен для разработки
    # на той же машине, где крутится БД. Имена uea/ueba_pass/ueba —
    # техническая учётка для сервиса, не путать с пользовательскими ролями.
    # В production db_password обязательно переопределяется через .env
    # или переменную окружения, чтобы не хранить секрет в коде.

    # ── Путь к датасету CERT r4.2 ────────────────────────────────────────
    dataset_path: str = "D:/Политех/Мага/Дипломы/М/12841247/r4.2"
    ground_truth_path: str = "D:/Политех/Мага/Дипломы/М/12841247/answers/insiders.csv"
    # Жёсткая привязка к локальной FS допустима на этапе исследований.
    # Позже путь можно заменить на переменную DATASET_PATH или вынести
    # загрузку данных в отдельный сервис MinIO/S3.

    # ── Гиперпараметры модели-детектора аномалий ─────────────────────────
    model_version: str = "1.0.0"
    mlflow_tracking_uri: str = "sqlite:///mlruns.db"
    # Версия архитектуры модели. Нужна для отслеживания экспериментов
    # и возможности восстановить конкретную обученную модель.

    training_epochs: int = 50
    # Максимум эпох обучения. Ограничение, чтобы обучение не ушло
    # в бесконечный цикл при отсутствии early stopping.

    batch_size: int = 128
    # Размер мини-батча. 128 — компромисс между скоростью обучения
    # и потреблением видеопамяти/ОЗУ. На слабых GPU можно уменьшить.

    learning_rate: float = 0.001
    # Начальный шаг градиентного спуска. 0.001 (1e-3) — стандартная
    # отправная точка для Adam-оптимизатора; обычно даёт сходимость
    # за разумное число эпох без расходимости.

    early_stop_patience: int = 7
    # Если валидационная метрика не улучшается 7 эпох подряд — обучение
    # останавливается. Предотвращает переобучение и экономит время.

    anomaly_threshold_percentile: float = 95.0
    # Порог аномальности в процентилях. Если оценка аномальности образца
    # превышает 95-й перцентиль распределения на тренировочных данных,
    # образец маркируется как подозрительный. Значение подбирается
    # экспериментально; 95 — разумный баланс между полнотой и точностью.

    class Config:
        env_file = ".env"
        # Pydantic прочитает файл .env в корне бекенда (backend/.env) и
        # применит указанные там значения поверх умолчаний. Так можно
        # переключить БД, порты, ключи, не меняя исходный код.
        # Файл .env НЕ должен попадать в git — он добавлен в .gitignore.


# Единственный экземпляр настроек, импортируемый во все модули приложения.
# Паттерн «одиночка» (singleton) гарантирует, что конфигурация загружается
# однократно при старте и не перечитывается из файла на каждый запрос.
settings = Settings()
