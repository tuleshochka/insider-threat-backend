"""Multi-input neural network with focal loss and attention fusion for insider threat detection."""
import torch
import torch.nn as nn
import torch.nn.functional as F


class FocalLoss(nn.Module):
    """
    Функция потерь Focal Loss для борьбы с сильным дисбалансом классов.

    В задачах обнаружения внутренних угроз аномалий (положительный класс)
    значительно меньше, чем нормального поведения. Обычная бинарная
    кросс-энтропия (BCE) переобучается на лёгких «нормальных» примерах и
    игнорирует редкие аномалии. Focal Loss модифицирует BCE так, чтобы
    модель штрафовалась сильнее за ошибки на «трудных» (плохо
    классифицированных) примерах и меньше — за уже хорошо
    классифицированные.
    """

    def __init__(self, alpha: float = 0.75, gamma: float = 2.0):
        """
        alpha : float = 0.75
            Весовой коэффициент для балансировки классов. Умножает потери
            для положительного (аномального) класса на alpha, а для
            отрицательного — на (1 - alpha). Значение 0.75 указывает, что
            аномалии в 3 раза важнее нормы, что соответствует ожидаемой
            доле аномалий в датасете CERT (~25-30% пользователей —
            инсайдеры). Это эвристика: alpha выбирается эмпирически так,
            чтобы приблизительно равняться доле положительного класса либо
            быть несколько выше неё для дополнительного фокуса на угрозах.

        gamma : float = 2.0
            Параметр фокусировки. Чем выше gamma, тем сильнее подавляются
            потери на лёгких (уже правильно классифицированных) примерах.
            gamma = 2.0 — наиболее распространённое значение из оригинальной
            статьи Lin et al. (Focal Loss for Dense Object Detection, 2017).
            Оно даёт хороший компромисс: «лёгкий» пример с pt = 0.9
            получает вес (1 - 0.9)^2 = 0.01, то есть почти исключается из
            обучения; «трудный» пример с pt = 0.1 — вес 0.81, то есть
            сохраняет почти полный вклад в градиент.
        """
        super().__init__()
        self.alpha = alpha
        self.gamma = gamma

    def forward(self, inputs: torch.Tensor, targets: torch.Tensor) -> torch.Tensor:
        """
        Вычисление Focal Loss.

        Шаги вычисления:
        1. BCE без редукции — получаем потери для каждого элемента батча.
        2. pt = exp(-bce) — вероятность правильной классификации (confidence).
           Если BCE мала (модель уверена), pt близка к 1; если BCE велика
           (модель ошибается), pt близка к 0.
        3. (1 - pt)^gamma — модулирующий множитель: чем увереннее модель,
           тем меньше вес примера, чем неувереннее — тем больше.
        4. alpha-балансировка — дополнительный вес для каждого класса.
        5. Итог: focal * weights, затем усреднение по батчу.

        Разделение модулирующего множителя (gamma) и весов классов (alpha)
        позволяет независимо управлять фокусировкой на трудных примерах
        и балансировкой между классами.
        """
        bce = F.binary_cross_entropy_with_logits(inputs, targets, reduction="none")
        # pt — «уверенность» модели в правильном ответе (probability of true class)
        pt = torch.exp(-bce)
        # Модулирующий множитель: (1 - pt)^gamma
        focal = self.alpha * (1 - pt) ** self.gamma * bce
        # Балансировка alpha для каждого класса отдельно
        weights = targets * self.alpha + (1 - targets) * (1 - self.alpha)
        return (focal * weights).mean()


class Branch(nn.Module):
    """
    Ветвь обработки одной группы признаков.

    Каждый тип признаков (логи, почта, устройство и т.д.) обрабатывается
    собственной небольшой нейронной сетью. Это позволяет модели извлекать
    специфичные для каждой модальности представления перед их объединением.

    Архитектура Linear -> ReLU -> Dropout -> Linear -> ReLU:

    — Два линейных слоя: первый расширяет размерность (in_dim -> hidden),
      второй сжимает до единого латентного пространства (hidden -> out_dim).
      Два слоя поверх одного дают больше выразительной способности, чтобы
      каждая ветвь могла выучить нелинейные зависимости в своих признаках.

    — ReLU: нелинейная активация между слоями, без неё стэкинг линейных
      слоев был бы эквивалентен одному слою (композиция линейных функций —
      линейна). ReLU проста, эффективна и не страдает от проблемы
      затухающих градиентов в глубоких сетях.

    — Dropout(0.3): регуляризация для борьбы с переобучением. В процессе
      обучения 30% нейронов случайно обнуляются на каждом шаге, что
      заставляет сеть не полагаться на отдельные нейроны и выучивать
      более устойчивые признаки. Значение 0.3 — эмпирический компромисс,
      достаточно для регуляризации небольшой сети. Dropout неактивен
      в режиме eval().
    """

    def __init__(self, in_dim: int, hidden: int = 16, out_dim: int = 8):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(in_dim, hidden),
            nn.ReLU(),
            nn.Dropout(0.3),
            nn.Linear(hidden, out_dim),
            nn.ReLU(),
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.net(x)


class AttentionFusion(nn.Module):
    """
    Внимательное слияние представлений ветвей (attention fusion).

    Простое объединение (конкатенация) всех ветвей даёт каждой ветви
    одинаковый вес вне зависимости от того, насколько информативны её
    признаки для конкретного примера. Attention-механизм решает эту
    проблему: для каждого батча динамически вычисляется, какие ветви
    важнее, и формируется взвешенный контекстный вектор.

    Дополнительно к взвешенной сумме конкатенируется полный вектор всех
    ветвей. Зачем нужны оба представления:
    — Взвешенная сумма (weighted): «сжатый» контекст с фокусом на
      важнейших ветвях. Потеря информации о менее весомых ветвях.
    — Конкатенация (concat): все исходные представления без потерь.
      Полная информация, но без приоритизации.
    — Объединение обоих: модель может использовать и фокусировку, и
      полные данные, что напоминает skip-connection в ResNet — градиент
      легче течёт, и модель сама решает, чему доверять больше.

    Вычисление внимания:
    1. Объединяем латентные векторы всех ветвей в один стек.
    2. Пропускаем каждый вектор через линейный слой без смещения:
       attn_project(latent_i) — скалярный «скop» важности для каждой
       ветви. Выбор Linear(latent_dim, 1, bias=False) — простейшая
       форма внимания (additive attention без bias), параметров мало,
       переобучения не возникает.
    3. Softmax по ветвям — получаем распределение вероятностей
       (веса от 0 до 1, сумма 1).
    4. Weighted sum — умножаем каждый латентный вектор на его вес.
    5. Конкатенируем weighted_sum с полной конкатенацией всех ветвей.
    6. Финальный Fusion MLP: два линейных слоя с ReLU и Dropout,
       сжимающие объединённый вектор в одно число (логит).
    """

    def __init__(self, branch_names: list[str], latent_dim: int = 8):
        """
        branch_names : имена ветвей (нужны для отладки/интерпретации).
        latent_dim : размерность выхода каждой ветви (out_dim из Branch).

        total = n * latent_dim + latent_dim
        Первое слагаемое — конкатенация всех ветвей (n штук).
        Второе — взвешенная сумма (latent_dim).
        """
        super().__init__()
        self.branch_names = branch_names
        n = len(branch_names)
        # Проекция каждого латентного вектора в скаляр — скор важности
        self.attn_project = nn.Linear(latent_dim, 1, bias=False)
        total = n * latent_dim + latent_dim  # concat + weighted
        self.fusion = nn.Sequential(
            nn.Linear(total, 32), nn.ReLU(), nn.Dropout(0.3),
            nn.Linear(32, 1),
        )

    def forward(self, latents: list[torch.Tensor]) -> tuple[torch.Tensor, torch.Tensor]:
        """
        latents : список тензоров [(batch, latent_dim), ...] для каждой ветви.

        Возвращает (logits, attn_weights). attn_weights возвращаются
        для интерпретации: можно посмотреть, какие ветви модель считает
        важными для каждого примера.
        """
        # Стек ветвей: (batch, n_branches, latent_dim)
        stacked = torch.stack(latents, dim=1)
        # Скоры: (batch, n_branches)
        scores = self.attn_project(stacked).squeeze(-1)
        # Нормализация Softmax по ветвям
        attn_weights = F.softmax(scores, dim=1)
        # Взвешенная сумма: (batch, latent_dim)
        weighted = (attn_weights.unsqueeze(-1) * stacked).sum(dim=1)
        # Полная конкатенация: (batch, n_branches * latent_dim)
        concat = stacked.view(stacked.size(0), -1)
        # Объединение: (batch, total)
        fused = torch.cat([weighted, concat], dim=1)
        return self.fusion(fused), attn_weights


class MultiInputClassifier(nn.Module):
    """
    Полная модель: несколько ветвей + внимание + классификатор.

    Принимает на вход словарь {имя_группы: тензор_признаков}.
    Каждая группа обрабатывается своей ветвью, затем AttentionFusion
    динамически агрегирует представления, и финальный слой выдаёт
    логит для бинарной классификации.

    Такая архитектура (multi-input + late fusion с attention) выбрана
    потому что:
    — Разные типы активности пользователя (логин, файлы, почта,
      устройства) имеют разную размерность и семантику. Их нельзя
      просто склеить до обработки — нужна отдельная ветвь под каждый
      тип.
    — Late fusion (объединение после отдельных ветвей) сохраняет
      внутреннюю структуру каждой модальности.
    — Attention поверх fusion позволяет модели адаптивно решать,
      какая группа признаков важна для конкретного пользователя
      в конкретный момент (например, при атаке через смену устройств
      ветвь device станет важнее остальных).

    predict_proba : обёртка для получения вероятности через сигмоиду.
    get_attention : возвращает не только предсказание, но и веса
      внимания — ключевая возможность для объяснимости (XAI).
    """

    def __init__(self, branches: dict[str, int], latent_dim: int = 8):
        """
        branches : словарь {имя_группы: размерность_признаков}.
                   Например: {"logon": 12, "file": 20, "email": 15, "device": 8}.
        latent_dim : размер единого латентного пространства для всех ветвей.
                     Все ветви сжимаются до одинаковой размерности, чтобы
                     attention-fusion мог работать с единообразными векторами.
        """
        super().__init__()
        self.branch_names = list(branches.keys())
        # Создаём модуль для каждой ветви. hidden = max(in_dim * 2, 16)
        # гарантирует, что первый слой ветви как минимум вдвое шире входа,
        # а минимальная ширина — 16, чтобы даже у маломерных ветвей была
        # достаточная ёмкость.
        self.branch_modules = nn.ModuleDict({
            name: Branch(in_dim, hidden=max(in_dim * 2, 16), out_dim=latent_dim)
            for name, in_dim in branches.items()
        })
        self.fusion = AttentionFusion(self.branch_names, latent_dim=latent_dim)

    def forward(self, inputs: dict[str, torch.Tensor]) -> torch.Tensor:
        """
        inputs : {имя_группы: тензор признаков (batch, in_dim)}.
        Каждая ветвь обрабатывает свою группу, затем AttentionFusion
        объединяет результаты в логит.
        """
        latents = [self.branch_modules[name](inputs[name]) for name in self.branch_names]
        logits, attn_weights = self.fusion(latents)
        return logits

    def predict_proba(self, inputs: dict[str, torch.Tensor]) -> torch.Tensor:
        """Возвращает вероятность принадлежности к аномальному классу."""
        return torch.sigmoid(self.forward(inputs))

    def get_attention(self, inputs: dict[str, torch.Tensor]) -> tuple[torch.Tensor, torch.Tensor]:
        """
        Возвращает (логит, веса_внимания) для интерпретации.

        Веса внимания показывают, насколько каждая группа признаков
        повлияла на решение — это позволяет специалисту по ИБ понять,
        какой тип активности (логин, файловые операции, почта)
        стал решающим при детектировании угрозы.
        """
        latents = [self.branch_modules[name](inputs[name]) for name in self.branch_names]
        logits, attn_weights = self.fusion(latents)
        return logits, attn_weights
