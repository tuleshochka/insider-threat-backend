"""Изолирующий лес — обёртка для сравнения алгоритмов обнаружения аномалий."""
import numpy as np
from sklearn.ensemble import IsolationForest


def train_isolation_forest(
    X: np.ndarray,
    contamination: float = 0.05,
    n_estimators: int = 100,
) -> IsolationForest:
    """
    Обучает модель изолирующего леса на нормализованной матрице признаков.

    Параметр contamination=0.05 означает, что алгоритм априори считает 5%
    наблюдений аномальными. Это значение выбрано по двум причинам: (1) в
    реальных корпоративных сетях доля подозрительных действий среди всех
    событий редко превышает 1–5 %; (2) датасет CERT r4.2, на котором
    проводится экспериментальная оценка, содержит около 5 % инсайдерских
    операций. Соответствие доли аномалий в обучающей выборке реальной доле
    в данных критически важно: заниженное значение приведёт к пропуску
    атак (false negatives), завышенное — к ложным срабатываниям (false
    positives).

    Параметр n_estimators=100 задаёт количество деревьев в лесу. Сто
    деревьев — эмпирически устоявшийся компромисс между качеством
    разделения и вычислительной стоимостью. Меньшее число деревьев
    (например, 50) увеличивает дисперсию оценки и снижает устойчивость
    результата; большее (200–500) дает незначительный прирост точности,
    но линейно увеличивает время обучения и инференса. Для датасета
    объёмом порядка десятков тысяч записей 100 деревьев обеспечивают
    стабильную сходимость.

    Параметр random_state=42 фиксирует зерно генератора случайных чисел.
    Это гарантирует воспроизводимость эксперимента: при повторных
    запусках с теми же данными модель строит одни и те же деревья, что
    необходимо для корректного сравнения алгоритмов в рамках
    диссертационного исследования. Без фиксации зерна каждое обучение
    давало бы слегка отличающиеся результаты, и оценить вклад метода
    стало бы невозможно.
    """
    model = IsolationForest(
        n_estimators=n_estimators,
        contamination=contamination,
        random_state=42,
        n_jobs=-1,  # использовать все доступные ядра процессора
    )
    model.fit(X)
    return model


def score_samples(model: IsolationForest, X: np.ndarray) -> np.ndarray:
    """
    Возвращает оценки аномальности в диапазоне [0, 1],
    где 1 — максимально аномальная точка, 0 — нормальная.

    Исходный метод decision_function библиотеки scikit-learn возвращает
    отрицательные значения для аномалий и положительные для нормальных
    объектов, причём шкала не нормирована и зависит от глубины деревьев.
    Такой вывод неудобен для интерпретации и последующего сравнения с
    другими детекторами (например, автоэнкодером или LSTM).

    Преобразование в диапазон [0, 1] решает три задачи:
      (1) унифицирует шкалу для всех алгоритмов — порог аномалии можно
          задавать единым числом (например, 0.7) независимо от метода;
      (2) делает оценку интуитивно понятной: чем ближе к единице, тем
          подозрительнее объект;
      (3) позволяет строить ROC-кривую и вычислять AUC, варьируя порог.

    Формула normalised = 1 - (raw - raw.min()) / (raw.max() - raw.min() + 1e-10)
    работает следующим образом:
      - raw — это исходный вектор оценок decision_function.
      - (raw - raw.min()) сдвигает распределение так, чтобы минимум стал
        нулём.
      - (raw.max() - raw.min() + 1e-10) — размах распределения; добавка
        1e-10 предотвращает деление на ноль в вырожденном случае, когда
        все оценки одинаковы (размах равен нулю).
      - Деление нормирует значения в отрезок [0, 1], где 0 соответствует
        исходному минимуму (самые нормальные объекты), а 1 — исходному
        максимуму (самые аномальные).
      - Вычитание из единицы (1 - ...) инвертирует направление: в
        исходной шкале skLearn большие значения означают «норма», а
        малые (отрицательные) — «аномалия». После инверсии большие
        значения (близкие к 1) соответствуют аномалиям, что согласуется
        с интуицией и единой шкалой остальных детекторов.
    """
    raw = model.decision_function(X)
    normalised = 1 - (raw - raw.min()) / (raw.max() - raw.min() + 1e-10)
    return normalised
