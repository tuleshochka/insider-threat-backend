"""SHAP-based explanation of individual anomaly detections."""
import numpy as np
import torch
from torch.utils.data import DataLoader, TensorDataset

from app.models.autoencoder import Autoencoder


class Explainer:
    """Локальное объяснение важности признаков через SHAP.

    В отличие от классического KernelExplainer, который вычисляет Shapley-значения
    путём семплирования всевозможных коалиций признаков, здесь используется
    упрощённый подход: для каждого признака по очереди зануляется его значение
    (подставляется среднее по фону), и измеряется, насколько изменилась ошибка
    восстановления автоэнкодера. Изменение MSE интерпретируется как вклад признака
    в аномальность наблюдения.

    Это не точные Shapley-значения, но вычислительно эффективная аппроксимация,
    достаточная для ранжирования признаков по степени влияния на решение модели.
    """

    def __init__(self, model: Autoencoder, background: np.ndarray, feature_names: list[str] | None = None):
        """
        Параметры
        ----------
        model : Autoencoder
            Обученная модель автоэнкодера. Переводится в режим eval(), чтобы
            отключить dropout/batch-norm и получить детерминированные предсказания.
        background : np.ndarray
            Фоновый набор данных (обычно нормальные наблюдения из обучающей выборки).
            Используется как "опорное распределение" — подставляя средние значения
            признаков из фона, мы эмулируем отсутствие информации о признаке.
            Почему фон, а не нули или случайные значения? Потому что в SHAP
            отсутствие знания о признаке моделируется как матожидание предсказания
            по всем возможным значениям этого признака из распределения данных.
        feature_names : list[str] | None
            Человекочитаемые имена признаков. Если не переданы, используются
            заглушки feat_0, feat_1 и т.д. Нужен, чтобы на выходе объяснения
            были интерпретируемыми — вместо безликого "признак 42" мы видим
            "cpu_usage" или "num_failed_logins".
        """
        self.model = model
        self.model.eval()
        self.feature_names = feature_names
        # Ограничиваем фоновую выборку до 100 образцов.
        # Почему 100? Это эмпирический компромисс между точностью аппроксимации
        # матожидания (чем больше фон, тем точнее среднее) и скоростью вычислений.
        # Для каждого признака мы считаем среднее по фону — 100 образцов дают
        # приемлемую оценку, при этом полный перебор не становится слишком дорогим.
        n_bg = min(background.shape[0], 100)
        self.background = background[:n_bg]

    def explain(self, sample: np.ndarray) -> list[dict]:
        """Возвращает список признаков, отсортированный по убыванию |shap|.

        Для каждого признака поочерёдно заменяем его значение на среднее по
        фону и смотрим, как изменилась MSE реконструкции. Если MSE упала —
        признак "вносил вклад" в аномальность (его исходное значение модель
        не могла хорошо восстановить). Если MSE выросла — признак был "нормальным",
        и его подмена на среднее сделала наблюдение более аномальным.

        Параметры
        ----------
        sample : np.ndarray формы (n_features,)
            Аномальное наблюдение, для которого нужно получить объяснение.

        Возвращает
        -------
        list[dict]
            Список словарей вида {feature_name, feature_value, shap_value},
            отсортированный по убыванию абсолютного SHAP-значения.
        """
        # Переводим sample и background в тензоры один раз, чтобы не делать
        # это внутри цикла (ускорение ценой небольшой дополнительной памяти).
        sample_t = torch.tensor(sample, dtype=torch.float32).unsqueeze(0)
        bg_t = torch.tensor(self.background, dtype=torch.float32)

        # Вычисляем базовую MSE — ошибку реконструкции исходного sample.
        # Это аналог "базового значения" (base value) в SHAP: предсказание модели
        # без маскирования каких-либо признаков.
        with torch.no_grad():
            base_recon = self.model(sample_t)
            base_mse = torch.nn.functional.mse_loss(base_recon, sample_t, reduction="none").mean(dim=1).item()

        features: list[dict] = []
        for i in range(sample.shape[0]):
            # Для каждого признака i создаём копию sample, в которой признак i
            # заменён на среднее значение этого признака по фоновой выборке.
            # Почему среднее, а не, скажем, медиана? В классическом SHAP
            # отсутствие знания о признаке моделируется интегралом по всем
            # возможным значениям. Мы аппроксимируем этот интеграл средним
            # по empirical distribution фона — быстрее и достаточно для ранжирования.
            masked = sample.copy()
            masked[i] = self.background[:, i].mean()
            masked_t = torch.tensor(masked, dtype=torch.float32).unsqueeze(0)
            with torch.no_grad():
                recon = self.model(masked_t)
                mse = torch.nn.functional.mse_loss(recon, masked_t, reduction="none").mean(dim=1).item()
            # SHAP-значение = разница между MSE с замаскированным признаком
            # и базовой MSE. Положительное значение означает, что подмена признака
            # на "нормальное" значение снизила ошибку → признак был аномальным.
            # Отрицательное — признак был нормальным, а его замена сделала
            # наблюдение более аномальным.
            shap_val = mse - base_mse
            fname = self.feature_names[i] if self.feature_names and i < len(self.feature_names) else f"feat_{i}"
            features.append({
                "feature_name": fname,
                "feature_value": float(sample[i]),
                "shap_value": round(float(shap_val), 6),
            })

        # Сортировка по убыванию абсолютного SHAP-значения.
        # Почему abs? Знак показывает направление влияния(положительное = признак
        # делает sample более аномальным), а модуль — силу влияния.
        # При построении отчёта для специалиста по ИБ важнее всего показать
        # наиболее "подозрительные" признаки первыми, независимо от знака.
        features.sort(key=lambda x: abs(x["shap_value"]), reverse=True)
        return features
