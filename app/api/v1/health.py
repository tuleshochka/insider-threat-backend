"""Health check endpoint."""
from fastapi import APIRouter
from app.models.schemas import HealthOut

router = APIRouter()


# Эндпоинт проверки работоспособности (health check).
# Используется:
# - контейнеризатором (Docker/Kubernetes) для liveness-пробы;
# - балансировщиком нагрузки для проверки, что экземпляр сервиса готов
#   принимать трафик;
# - CI/CD-пайплайном перед переключением трафика на новую версию.
#
# Метод GET выбран потому, что запрос идемпотентен: он не меняет состояние
# сервера, а только возвращает заранее известную структуру. Это стандарт
# REST — GET для чтения, и health check считается чтением статуса.
#
# response_model=HealthOut гарантирует, что клиент всегда получит
# предсказуемую структуру JSON, даже если в будущем в модель добавятся
# новые поля. FastAPI автоматически сериализует ответ и валидирует его
# по Pydantic-схеме, что исключает случайную утечку внутренних данных.
@router.get("", response_model=HealthOut)
def health_check():
    return HealthOut()
