"""Точка входа FastAPI-приложения.
При запуске (uvicorn app.main:app) этот модуль собирает всё воедино:
конфигурацию, CORS, роуты, подключение к БД.
"""
from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware

from app.config import settings
from app.models.db import engine, Base
from app.models import orm  # регистрирует ORM-модели в Base (побочный эффект импорта)
from app.api.v1.router import api_router

# ── Создание экземпляра FastAPI ──────────────────────────────────────────
app = FastAPI(
    title=settings.app_name,
    # Название берется из конфига, чтобы OpenAPI-документация (Swagger)
    # автоматически отображала имя системы, заданное в .env.
    version=settings.model_version,
    # Версия API привязана к версии модели — каждый раз, когда архитектура
    # детектора меняется, версия растёт, и клиенты увидят это в Swagger.
    docs_url="/docs",
    # Интерактивная документация по адресу /docs. В production, чтобы не
    # светить эндпоинты, docs_url можно выставить в None.
)

# ── CORS (Cross-Origin Resource Sharing) ─────────────────────────────────
app.add_middleware(
    CORSMiddleware,
    allow_origins=[
        "http://localhost:3000",   # React-фронтенд (create-react-app, порт 3000)
        "http://127.0.0.1:3000",   # loopback-алиас (иначе CORS-политика браузера
                                   # считает localhost и 127.0.0.1 разными origin'ами)
        "http://localhost:5173",   # Vite-фронтенд (стандартный порт Vite)
        "http://127.0.0.1:5173",  # тот же хост, другой IP-формат
    ],
    allow_credentials=True,
    # Разрешает передачу cookie и заголовков авторизации. Необходимо для
    # сессионной аутентификации, если она будет внедрена позже.
    allow_methods=["*"],
    # Разрешены все HTTP-методы (GET, POST, PUT, DELETE, и т.д.).
    # В production список лучше сузить до реально используемых.
    allow_headers=["*"],
    # Разрешены все заголовки. На практике "*" безопасен, если сервер
    # не доверяет непроверенным origin'ам (а здесь они жёстко заданы выше).
)
# ── Почему такие origin'ы? ───────────────────────────────────────────────
# React dev-сервер по умолчанию слушает порт 3000, Vite — 5173.
# Без добавления этих origin'ов в CORS браузер блокирует запросы
# с фронтенда к API на другом порту. Оба варианта (localhost и 127.0.0.1)
# нужны, потому что браузер различает их как разные источники.

# ── Подключение роутера ──────────────────────────────────────────────────
app.include_router(api_router, prefix="/api/v1")
# Все эндпоинты, определённые в api_router, монтируются под префиксом /api/v1.
# Это стандартная практика версионирования API: если в будущем понадобится
# ломающая обратную совместимость версия, её можно добавить как /api/v2,
# не удаляя v1. Клиенты старой версии продолжат работать.

# ── Жизненный цикл: при старте сервера ───────────────────────────────────
@app.on_event("startup")
def on_startup():
    """Создание таблиц БД при первом запуске (для удобства разработки).

    Base.metadata.create_all смотрит на все модели, унаследованные от Base,
    и создаёт соответствующие таблицы, если их ещё нет в PostgreSQL.

    Почему это здесь, а не в миграциях Alembic? На стадии прототипирования
    миграции только замедляют итерации — таблицы пересоздаются заново при
    каждом изменении моделей. Однако перед выкатом в эксплуатацию этот
    вызов нужно убрать и перейти на Alembic, чтобы не потерять данные.
    """
    Base.metadata.create_all(bind=engine)
